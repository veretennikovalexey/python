import random

number = random.randint(1, 100) # загадываем случайное число
attempts = 0 # счетчик попыток
max_attempts = 10 # максимальное количество попыток

while attempts < max_attempts: # пока не исчерпан лимит попыток
    attempts += 1 # увеличиваем счетчик попыток
    print(f"Попытка {attempts}") # выводим номер попытки
    guess = int(input("Введите число: ")) # просим пользователя ввести число
    if guess == number: # если угадал
        print("Поздравляю, вы угадали!") # поздравляем
        break # выходим из цикла
    elif guess < number: # если число меньше загаданного
        print("Ваше число меньше загаданного") # подсказываем
    else: # если число больше загаданного
        print("Ваше число больше загаданного") # подсказываем
else: # если исчерпан лимит попыток
    print(f"Вы проиграли. Загаданное число было {number}") # сообщаем правильный ответ
